#include <stdio.h>


/** \brief Realiza la suma de dos numeros.
 *
 * \param primerNumero float Recibe el primer numero.
 * \param segundoNumero float Recibe el segundo numero.
 * \return float Me devuelve la suma de ambos numeros.
 *
 */
float SumarNumeros(float primerNumero, float segundoNumero);

/** \brief Realiza la resta de dos numeros.
 *
 * \param primerNumero float Recibe el primer numero.
 * \param segundoNumero float Recibe el segundo numero;
 * \return float Me devuelve el resultado del primer numero menos el segundo numero.
 *
 */
float RestarNumeros(float primerNumero, float segundoNumero);

/** \brief Realiza la multiplicacion de dos numeros.
 *
 * \param primerNumero float Recibe el primer numero.
 * \param segundoNumero float Recibe el segundo numero.
 * \return float Me devuelve el resultado de la multiplicacion de ambos numeros.
 *
 */
float MultiplicarNumeros(float primerNumero, float segundoNumero);

/** \brief Realiza la division de dos numeros.
 *
 * \param primerNumero float Recibe el primer numero.
 * \param segundoNumero float Recibe el segundo numero.
 * \return float Me devuelve la division del primer numero dividido el segundo.
 *
 */
float DividirNumeros(float primerNumero, float segundoNumero);

/** \brief Calcula el factorial de un numero entero
 *
 * \param numero int recibe el numero
 * \return int devuelve el factorial del numero ingresado
 *
 */
int CalcularFactorial(int numero);
